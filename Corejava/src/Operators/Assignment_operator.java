package Operators;

public class Assignment_operator {
	public static void main(String[] args) {
		
		int a = 27;
		int b = 3;
		 
		System.out.println("Assignment operator");
		System.out.println("================================================");
		
		System.out.println("Assignment operator perform on a = ");
		
		a+=4;
		System.out.println("Add 4 = "+a);
	
        a-=4;
		System.out.println("subtract 4 = "+a);

		a*=4;
		System.out.println("multiply 4 = "+a);
	
		a/=4;
		System.out.println("division 4 = "+a);
	
		a%=4;
		System.out.println("modulo 4 = "+a);
	
        System.out.println("Assignment operator perform on b = ");
		
		b+=4;
		System.out.println("Add 4 = "+b);
	
        b-=4;
		System.out.println("subtract 4 = "+b);

		b*=4;
		System.out.println("multiply 4 = "+b);
	
		b/=4;
		System.out.println("division 4 = "+b);
	
		b%=4;
		System.out.println("modulo 4 = "+b);
		
		
	}

}
