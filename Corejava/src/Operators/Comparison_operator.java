package Operators;

public class Comparison_operator {
	public static void main(String[] args) {
		
		int a = 46;
		int b = 86;
		
		System.out.println("Comparision operator");
		
		System.out.println("================================================");
		
		System.out.println("a is less than b = "+(a<b));
		
		System.out.println("a is greater than b = "+(a>b));
		
		System.out.println("a is less than equal b = "+(a<=b));
		
		System.out.println("a is greater than equal b = "+(a>=b));
		
		System.out.println("a is not equal b = "+(a!=b));
		
		System.out.println("a is equal equal b = "+(a==b));
	}

}
