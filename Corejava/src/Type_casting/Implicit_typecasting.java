package Type_casting;

public class Implicit_typecasting {
	public static void main(String[] args) {
		 System.out.println("Implicit type casting");
		 System.out.println("================================================");
			

		 	
   /* 1-The small data type is convert big data type it is  
	    called Implicit type casting.
	       
	  2-In Implicit type casting is automatically performed by
	    the compiler.
	              
   */
		 
		 int a = 10;
		  
		 double b = a;
		 System.out.println(b);

	}

}
